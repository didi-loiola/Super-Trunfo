var cartas = [superTrunfo, cartaPaulo, cartaRafa, cartaGui, cartaLol, cartaNaruto, cartaHarry, cartaBatman, cartaMarvel, carta3, carta4, carta5, carta6, carta7, carta8, carta9, carta10, carta11, carta12, carta13]

var pontosJogador = 0
